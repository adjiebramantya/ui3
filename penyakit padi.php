			<h3> Penyakit Tanaman Padi dan Cara Mengatasinya</h3>
			<ol>
			<li><b>PENYAKIT BERCAK DAUN COKLAT</b></li>

			<p>Penyebab : Jamur helmintosporium oryzae.Gejala : menyerang pelepah,malai,buah yang baru tumbuh dan bibit yang baru berkecambah,biji bercak bercak coklat tetapi tetap berisi, padi dewasa busuk kering, biji kecambah busuk dan kecambah mati.</p>

			<p>Pengendali :
			1. Meredam benih di air hangat + pocnasa.
			2. Pemupukan berimbang.
			3. Tanam padi tahan penyakit ini.</p>

			<li><b>PENYAKIT BLAST</b></li>

			<p>Penyebab jamur pyricularia oryzae. Gejala menyerang daun ,buku pada malai dan ujung tangkai malai, daun, gelang buku, tangkai malai dan cabang di dekat pangkal malai membusuk, pemasakan makanan terhambat dan butiran padi menjadi hampa.</p>

			<p>Pengendali :
			1. Membakar sisa jerami,menggenangi sawah, menanam varitas unggul sentani ,cimandiri IR-48,IR-36,pemberian pupuk N di saat pertengahan fase vegetatif dan fase pembentukan bulir.
			2. Pemberian glio di awal tanam.</p>

			<li><b>BUSUK PELEPAH DAUN.</b></li>

			<p>Penyebab : jamur rhizoctonia sp.gejala : menyerang daun dan pelepah daun pada tanaman yang telah membentuk anakan,menyebabkan jumlah dan mutu gabah menurun.

			Pengendali :
			1. Menanam padi tahan penyakit.
			2. Pemberian glio pada saat pembentukan anakan.</p>

			<li><b>PENYAKIT FUSARIUM</b></li>

			<p>Penyebab : Jamur fusarium moniliforme.Gejala : menyerang malai dan biji muda menjadi kecoklatan, daun terkulai,akar membusuk.</p>

			<p>Pengendali:
			1. Merenggangkan jarak tanam.
			2. Mencelupkan benih + pocnasa dan di sebari glio di lahan.</p>
			<center><img src="image/padirusak.PNG"></center>
			<li><b>PENYAKIT KRESEK / HAWAR DAUN</b></li>

			<p>Penyebab : Bakteri xanthomonas campestrys pv oryzae.
			gejala : menyerang daun dan titik tumbuh.terdapat garis garis di antara tulang daun,garis melepuh dan berisi cairan kehitam hitaman,daun mengering dan mati.</p>

			<p>Pengendali :
			1. Menanam varitas tahan penyakit seperti IR 36,IR 46 cisadane,cipunegara,menghindari muka mekanis,sanitasi lingkungan.
			2. Pengendalian di awal dengan glio.</p>

			<li><b>PENYAKIT KERDIL</b></li>

			<p>Penyebab : Virus di tularkan oleh wereng coklat nilaparvata lugens.
			gejala menyerang semua bagian tanaman ,daun menjadi pendek ,sempit berwarna hijau kekuning kuningan, batang pendek, buku buku pendek, anakan banyak tetapi kecil.</p>

			<p>Pengendalian : Sulit dilakukan, usaha pencegahan dengan memusnahkan tanaman yang terserang dan mengendalikan vektor dengan BVR atau pestona.</p>

			<li><b>PENYAKIT TUNGAU</b></li>

			<p>Penyebab : Virus yang di tularkan oleh wereng hijau NEPHOTETTIX IMPICTICEPS. Gejala menyerang semua bagian tanaman, pertumbuhan tanaman kurang sempurna, daun kuning hingga kecoklatan, jumlah tunas berkurang, pembungaan tertunda, malai kecil dan tidak berisi.</p>

			<p>Pengendalian : Menanam padi tahan wereng seperti Kelara, IR 52, IR 36, IR 48, IR 54, IR 46, IR 42 dan mengendalikan vektor virus dengan BVR.</p>

			<p>Catatan : jika pengendalian hama penyakit dengan menggunakan pestisida alami belum mengatasi, dapat dipergunakan pestisida kimia yang di anjurkan. agar pestisida kimia lebih merata dan tidak mudah hilang oleh air hujan, tambahkan perekat perata AERO 810.</p>


			</ol>
			
			
