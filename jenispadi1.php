			<h3> Jenis-jenis Padi</h3>
			<ol>
				<li><b>Varietas Padi Hibrida</b></li>
				<p>Varietas hibrida adalah varietas padi yang hanya sekali tanam. Kelebihan padi varietas hibrida adalah potensi hasil panen yang maksimal. Hasil panen dapat mencapai dua kali lipat dari padi lokal. Butiran padi yang dihasilkan lebih bagus, dengan kualitas nasi yang lebih pulen dan wangi.</p>
				<p>Jenis varietas padi hibrida antara lain Intani 1 dan 2, Rokan, SL 8 dan 11 SHS, Segera Anak, PP1, H1, Bernas Prima, SEMBADA B3, B5, B8 DAN B9, Long Ping (pusaka 1 dan 2), Adirasa-1, Adirasa-64,  Hibrindo R-1, Hibrindo R-2, Manis-4 dan 5, Hipa4, Hipa 5 Ceva, Hipa 6 Jete, Hipa 7-10 11, MIKI 1-3, SL 8 SHS, SL 11 HSS dan Maro.</p>
				<li><b>Varietas Padi Unggul</b></li>
				<p>Varietas padi unggul berada satu tingkat di bawah varietas hibrida. Varietas ini dapat ditanam berkali-kali dengan kualitas yang sama. Artinya, hasil panen dari varietas padi unggul masih bisa dijadikan benih.</p>
				<p>Beberapa contoh varietas padi unggul antara lain adalah Inpara 1-8, Inpago 1-5, Inpari 1-21, Inpari 31, Inpari 33, Inpari 34 Salin Agritan, dan Inpari 35 Salin Agritan.</p>
				<li><b>Varietas Padi Lokal</b></li>
				<p>Varietas padi lokal adalah varietas padi yang khusus berada di daerah tertentu. Varietas semacam ini hanya cocok ditanam di daerah tertentu saja, karena membutuhkan spesifikasi khusus untuk tumbuh dan memproduksi padi.</p>
				<p>Jenis jenis padi lokal antara lain : Gropak (Kulon Progo), Indramayu, Dharma Ayu, Srimulih, Andel Jaran, Merong, Gundelan, Marong, Simenep, dan Ketan Lusi.</p>

			</ol>
			
			
