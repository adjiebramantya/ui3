			<h3> Cara Mendapatkan Benih Padi Unggul</h3>
			<ol><center><img src="image/benihpadi.jpg"></center>
				<p>Saat ini di pasaran ada berbagai jenis bibit padi yang dijual. Dalam beberapa saat, banyak petani yang kecewa dengan hasilnya. Ada bibit padi yang ketika ditanam tapi produktivitas panennya jauh dari harapan, ada juga yang mudah terserang hama.</p>
				<p>Maka dari itu disarankan untuk petani membuat bibit padi sendiri.</p>
				<p>Petani bisa mendapatkan bibit unggul dari hasil panen sebelumnya. Jadi jika produktivitas lahan sawah tinggi, disarankan untuk menyisihkan 25 Kg (untuk 1 hektar sesuai anjuran pemerintah) gabah untuk dijadikan bibit di musim tanam selanjutnya.</p>
				<p>Selain produktivitas yang tinggi, hal lain yang perlu diperhatikan untuk menjadikan gabah menjadi bibit unggul adalah pastikan tanaman sebelumnya sehat dan tidak terserang hama penyakit.</p>
				<p>Dengan membuat bibit padi sendiri, petani bisa menghemat biaya produksi. Sekarang ini harga gabah kering panen di tingkat petani sekitar Rp 4.500/Kg, sedangkan harga bibit padi sekitar Rp 12.000-15.000/Kg.</p>
				<center><img src="image/benihpadikecil.jpg"></center>
				<p>Jika dihitung-hitung, dari pada membeli bibit padi seharga Rp 300.000-375.000 untuk satu hektar lahan sawah di musim tanam selanjutnya, lebih baik petani tidak menjual 25 Kg dari hasil panennya. Dengan begini, petani bisa menghemat Rp 187.500-262.500.</p>
				<p>Sekitar 60-65% peningkatan produktivitas usaha tani ditentukan oleh bibit yang unggul, maka dari itu memperhatikan kondisi bibit sangat penting dalam budi daya.</p>
				<p>Ada tiga tahap yang dilakukan untuk membuat bibit padi sendiri, yaitu tahap pascapanen, persiapan bibit dan penyemaian bibit menjadi benih.</p>

				<li><b>Pasca Panen Padi</b></li>
				<center><img src="Image/menjemurbibitpadi.jpg"></center>
				<p>Pemanenan padi yang akan dibuat benih unggul adalah ketika padi sudah menguning 85-92%. Hal ini supaya benih padi bisa maksimal. Dan tentunya jika terlalu tua juga dikhawatirkan terkena hama wereng.</p>
				<p>Pemanenan padi dilakukan khusus dengan sortir khusus untuk membuat benih padi yang unggul.</p>
				<p>Lakukan penjemuran padi pada alas yang lembut dan tidak di atas permukaan yang keras seperti lantai. Tapi Jemurlah bibit padi pada tenda atau terpal untuk menghindari terkelupasnya calon benih padi unggul.</p>
				<p>Pada cuaca yang cerah, gabah dijemur selama 3-4 hari sampai kadar air gabah mencapai 11-12%. Ini penting agar dalam penyimpanan, bibit tidak membusuk.</p>
				<p>Setelah penjemuran selesai diamkan selama beberapa hari supaya benih padi menjadi lebih dingin sebelum dilakukan perendaman.</p>

				<li><b>Persiapan Bibit</b></li>
				<p>Ketika sudah memasuki musim tanam, bibit bisa mulai dipersiapkan. Hal pertama yang harus dilakukan dalam mepersiapkan bibit adalah perendaman. Ini berfungsi untuk menyeleksi bibit mana yang akan digunakan.</p>
				<p>Saat perendaman perlu diperhatikan hal-hal berikut ini agar hasil yang didapat sesuai dengan keingan dan sesuai dengan lahan pertanian. Pastikan juga sesuai dengan metode tanam yang digunakan.</p>
				<p>ika menggunakan metode SRI maka petani cukup merendam 1-2 kg untuk 100 bata/ubin atau 5-6 kg untuk satu ha. Tentu sangat berbeda jika metode yang anda pilih adalah metode hazton atau yang lain.</p>
				<p>Siapkan air yang sudah mengandung garam, atau abu dengan kondisi jika telur dimasukkan maka akan melayang di tengah air.
				Setelah dilakukan perendaman maka akan terjadi perpisahan benih padi yang akan terbagi atas 3 kelompok yaitu bagian yang paling dasar atau tenggelam maka akan memiliki daya tumbuh 90-95 %,</p>
				<p>Sedangkan bagian kedua yang melayang maka padi akan memiliki daya tumbuh 85-90 %, Sedangkan bagian yang melayang di bagian atas maka memiliki daya tumbuh 0-85 %.</p>
				<p>Pilih bibit yang terendam dan melayang untuk ditanam. Direkomendasikan untuk tidak menggunakan bibit yang mengapung karena kemampuannya untuk tumbuh sangat kecil.</p>

				<li><b>Penyemaian Bibit</b></li>
				<p>Setelah didapat bibit yang akan ditanam, bibit bisa direndam air yang telah dicampur dengan sedikit hormon terlebih dahulu. Ada banyak produk hormone bibit yang bisa digunakan dan ditemukan di pasaran dengan kadar anjuran yang tertera di kemasan. Hormon yang digunakan untuk perkecambahan bibit adalah auksin dan giberelin.</p>
				<center><img src="Image/rendampadi.jpg"></center>
				<p>Rendam bibit selama 24 jam penuh hingga mulai muncul sedikit perakaran dari bibit. Jika belum keluar juga, teruskan perendaman sampai calon akar mulai muncul sebanyak 0,5 cm. Cek secara berkala sampai bibit siap disemai dalam tray.</p>
				<p>Siapkan media penyemaian berupa tanah ladu atau tanah sawah yang sudah dikeringkan terlebih dahulu. Tanah kemudian diayak untuk menghilangkan kerikil, lalu tanah diratakan dalam tray semai setebal kurang lebih 2 cm.</p>
				<p>Kemudian bibit yang sudah direndam bisa ditabur ke dalam media tray semai. Siram bibit secara berkala untuk memastikan bibit menerima cukup air untuk tumbuh baik. Setelah tunas-tunas mulai muncul, pindahkan tray ke lokasi terbuka agar benih menyerap cukup sinar matahari.</p>
				<center><img src="Image/benihpindahtanam.jpg"></center>
				<p>Setelah muncul 4-5 daun atau berumur 10-25 hari, benih sudah siap dipindah tanam. Sebab pada umur ini akar benih sudah cukup kokoh dan kuat. Namun di beberapa metode tanam, seperti metode hazton, umur benih siap tanam dianjurkan umur yang lebih tua, sebab lebih tahan terhadap hama keong emas dan produktivitasnya tidak menurun.</p>
				<p>Untuk daerah endemik keong emas, sangat dianjurkan untuk melakukan pindah tanam dengan umur benih yang cukup lama.</p>

