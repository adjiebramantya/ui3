			<h4> Pemupukan Tepat pada Tanaman Padi</h4>
			
			<p>Sebelumya telah dibahas tentang cara pemupukan yang tepat pada tanaman bawang merah. Kini saatnya mengulas tentang pemupukan tanaman padi seiring dengan keadaan di lapangan yang sedang musim tanam padi.</p>

			<p>Untuk memahami pemupukan pada tanaman padi, kita harus mengetahui umur tanaman padi terlebih dahulu. Sekarang ini banyak varietas padi berumur genjah yang dilepas oleh pemerintah.</p>

			<p>Contoh, Inpari 10 berumur 108-116 hari dan Inpari 13 berumur 103 hari. Sedangkan padi ciherang dan IR 64 umumnya berumur 115 -125 hari.</p>
			<center><img src="Image/artikelpupuk1.jpg" width="700px" height="400px"></center>
			<p>Dengan melihat dua kondisi yang berbeda ini, petani seringkali mengalami kesulitan untuk menentukan kapan waktu pemupukan yang tepat bagi keduanya.</p>

			<p>Teknik pemupukan tanaman padi memang sangat relatif, tidak ada ukuran secara pasti dosis dan waktu yang ditentukan, karena banyak sekali faktor yang harus diperhatikan.</p>

			<p>Struktur tanah dengan kondisi unsur hara yang berbeda-beda di tempat satu dengan yang lainnya, tentu juga memerlukan teknik yang berbeda dalam hal pemupukannya.</p>
			<center><img src="Image/artikelpupuk2.jpg" width="700px" height="400px"></center>
			<p>Salah satu contoh dosis, jenis pupuk dan waktu pemupukan yang tepat pada tanaman padi adalah sebagai berikut:

		    Pemupukan susulan pertama dilakukan saat padi berumur 7-10 HST. Pupuk yang digunakan adalah Urea 75 kg/ha, SP-36 100 kg/ha dan KCL 50 kg/ha.
		    Pemupukan susulan kedua diberikan saat tanaman padi berumur 21 HST menggunakan pupuk Urea sebanyak 150 kg/ha.
			Pemupukan susulan ketiga pada saat umur padi 42 HST menggunakan 75 kg/ha Urea dan 50 kg/ha KCl.</p>

			<p>Dari tiga kali pemupukan tersebut, dalam satu musim tanam padi pada luasan 1 hektar membutuhkan pupuk Urea (Nitogen) 300 kg, SP36/TSP (Phospor) 100 kg, dan KCl (Kalium) 100 kg.</p>
			<center><img src="Image/artikelpupuk3.png" width="700px" height="400px"></center>
			<p>Tanaman padi memerlukan banyak hara N dibanding hara P ataupun K. Pupuk Urea perlu diberikan sebanyak 3 kali, agar pemberian pupuk N menjadi lebih efisien terserap oleh tanaman padi. Sedangkan pemberian pupuk KCl dilakukan 2 kali, agar proses pengisian gabah menjadi lebih baik.</p>

			<p>Untuk memantau kecukupan pupuk Urea (Nitrogen) pada tanaman padi bisa menggunakan Bagan Warna Daun (BWD). Pada alat ini terdapat empat kotak skala warna, mulai warna hijau muda hingga hijau tua, yang menggambarkan tingkat kehijauan daun tanaman padi.</p>

			<p>Sebagai contoh, jika daun tanaman berwarna hijau muda berarti tanaman kekurangan hara N sehingga perlu dipupuk.</p> 
			<center><img src="Image/artikelpupuk4.jpg" width="700px" height="400px"></center>
			<p>Sebaliknya, jika daun berwarna hijau tua atau tingkat kehijauan daun sama dengan warna dikotak skala 4 pada BWD berarti tanaman sudah memiliki hara N yang cukup sehingga tidak perlu lagi dipupuk.</p>

			<p>Monitoring pemberian pupuk dengan alat BWD dilakukan sejak 14 HST sampai fase berbunga (63 HST) setiap 7 hari sekali.</p>

			<p>Hasil penelitian menunjukkan, pemakaian BWD dalam kegiatan pemupukan N dapat menghemat penggunaan pupuk urea sebanyak 15-20 % dari takaran yang umum digunakan petani padi tanpa menurunkan hasil.</p>
			<center><img src="Image/artikelpupuk6.jpg" width="700px" height="400px"></center>
			<p>Sementara itu, hara P dan K tidak perlu diberikan setiap musim. Hara P dapat diberikan tiap 4 musim sekali sedangkan hara K dapat diberikan setiap 6 musim sekali. Ini disebabkan karena pupuk P dan K yang telah diaplikasikan hanya sekitar 20 % dan 30 % nya terserap tanaman sedangkan sisanya terakumulasi dalam tanah.</p>

			<p>Selain pupuk kimia di atas, sangat dianjurkan untuk menambahkan pemberian pupuk organik. Pupuk organik yang dianjurkan berupa pupuk kandang atau kompos jerami sebanyak 2 ton per hektar setiap musim,</p>

			<p>Penggunaan pupuk organik ini dapat mengembalikan sifat-sifat tanah, memperbaiki struktur tanah, meningkatkan kesuburan serta menggemburkan tanah yang telah padat karena efek penggunaan pupuk anorganik atau pupuk kimia.</p>
